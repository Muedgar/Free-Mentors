function confirmAdminOne() {
   if (document.getElementById("tokenValueOne").value == "") {
   	document.getElementById("helpMeOne").innerHTML = "Required: *Confirm mentor id is yours";
   }else{
   	document.getElementById("helpMeOne").innerHTML = "You are the mentor";
   }
}

function changeToMentorOne() {
	if (document.getElementById("helpMeOne").innerHTML == "Required: *Confirm mentor id is yours" || document.getElementById("helpMeOne").innerHTML == "") {
         document.getElementById("helpMeOne").innerHTML = "Required: *Confirm mentor id is yours";
	}else{
		document.getElementById("accept-id-one").innerHTML = "User changed to mentor";
	}
}
